/* Program for summation of two numbers */

import java.util.*;
class ArifTask1 {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in); 
        System.out.println("Enter numbers:");
        int a=sc.nextInt();
        int b=sc.nextInt();
        System.out.println("Sum of "+a+" and "+b+" is "+(a+b));
    }
}
